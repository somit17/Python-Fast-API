# Comparisons:
num_1 = 3
num_2 = 2
# Equal:            3 == 2
print(num_1 == num_2)

# Not Equal:        3 != 2
print(num_1 != num_2)
# Greater Than:     3 > 2
print(num_1 > num_2)
# Less Than:        3 < 2
print(num_1<num_2)
# Greater or Equal: 3 >= 2
print(num_1>=num_2)
# Less or Equal:    3 <= 2
print(num_1<=num_2)


